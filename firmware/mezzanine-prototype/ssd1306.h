/* ----- Internal helpers ----- */

static inline void ssd1306_cmd_(i2c_inst_t *i2c, uint8_t cmd)
{
    uint8_t buf[2] = { 0x00, cmd };  /* 0x00 = command stream prefix */
    i2c_write_blocking(i2c, SSD1306_I2C_ADDR, buf, 2, false);
}

/* ----- Public API ----- */

static inline void ssd1306_init(i2c_inst_t *i2c)
{
    sleep_ms(100);  /* let display power settle */

    static const uint8_t init_seq[] = {
        0xAE,             /* display off */
        0xD5, 0x80,       /* clock divide: default ratio + freq */
        0xA8, 0x1F,       /* multiplex ratio = 31 (= 32 lines) */
        0xD3, 0x00,       /* display offset = 0 */
        0x40,             /* display start line = 0 */
        0x8D, 0x14,       /* charge pump enable */
        0x20, 0x00,       /* horizontal addressing mode */
        0xA1,             /* segment remap: column 127 maps to SEG0 */
        0xC8,             /* COM scan direction reversed */
        0xDA, 0x02,       /* COM pins hardware config (32-line panel) */
        0x81, 0x8F,       /* contrast = 0x8F */
        0xD9, 0xF1,       /* pre-charge period */
        0xDB, 0x40,       /* VCOMH deselect level */
        0xA4,             /* display follows RAM (not all-on) */
        0xA6,             /* normal (non-inverted) display */
        0xAF              /* display on */
    };
    for (size_t i = 0; i < sizeof(init_seq); i++) {
        ssd1306_cmd_(i2c, init_seq[i]);
    }
}

static inline void ssd1306_show(i2c_inst_t *i2c, const uint8_t *fb)
{
    /* Set column range 0..127, page range 0..3 */
    ssd1306_cmd_(i2c, 0x21); ssd1306_cmd_(i2c, 0); ssd1306_cmd_(i2c, 127);
    ssd1306_cmd_(i2c, 0x22); ssd1306_cmd_(i2c, 0); ssd1306_cmd_(i2c, 3);

    /* Send framebuffer with 0x40 data prefix */
    uint8_t buf[SSD1306_FB_SIZE + 1];
    buf[0] = 0x40;
    memcpy(buf + 1, fb, SSD1306_FB_SIZE);
    i2c_write_blocking(i2c, SSD1306_I2C_ADDR, buf, sizeof(buf), false);
}

static inline void ssd1306_text(uint8_t *fb, int col, int page, const char *s)
{
    if (page < 0 || page >= SSD1306_PAGES) return;
    uint8_t *p = fb + (page * SSD1306_WIDTH);
    while (*s && col + 5 <= SSD1306_WIDTH) {
        char c = *s++;
        if (c < 0x20 || c > 0x7E) c = '?';
        const uint8_t *glyph = SSD1306_FONT_5X7[c - 0x20];
        for (int i = 0; i < 5; i++) p[col + i] = glyph[i];
        col += 6;  /* 5px glyph + 1px spacing */
    }
}

