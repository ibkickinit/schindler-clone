/*
 * ssd1306.h - Minimal SSD1306 OLED driver for 128x32 displays over I2C
 *
 * Single-header library. Include in main.c.
 *
 * Framebuffer layout: 512 bytes = 128 cols * 4 pages.
 * Each page is 8 rows; byte stores 8 vertical pixels (bit 0 = top row).
 * Text rendering: 5x7 font + 1px spacing = 6px per char, 21 chars/line.
 *
 * Public API:
 *   void    ssd1306_init(i2c_inst_t *i2c)             - init display
 *   void    ssd1306_show(i2c_inst_t *i2c, const uint8_t *fb) - push 512-byte framebuffer
 *   void    ssd1306_text(uint8_t *fb, int col, int page, const char *s) - draw text
 *
 * I2C address fixed at 0x3C.
 */

